<template>
  <div class="col-3">
    <a href="#">
      <img :src="user.image" width="140px" height="140px" />
    </a>
    <router-link
      :to="{
        name: 'user-profile',
        params: {
          id: user.id,
        },
      }"
    >
      <h2>{{ user.name }}</h2>
    </router-link>
    <span class="badge badge-secondary"
      >追蹤人數：{{ user.FollowerCount }}</span
    >
    <p class="mt-3">
      <button
        type="button"
        class="btn btn-danger"
        v-if="user.isFollowed"
        @click.stop.prevent="deleteFollowed"
      >
        取消追蹤
      </button>
      <button
        type="button"
        class="btn btn-primary"
        @click.stop.prevent="addFollowed"
        v-else
      >
        追蹤
      </button>
    </p>
  </div>
</template>

<script>
export default {
  props: {
    foodGuru: {
      type: Array,
      required: true,
    },
  },
  data() {
    return { user: this.foodGuru };
  },
  methods: {
    addFollowed() {
      this.user = {
        ...this.user,
        isFollowed: true,
      };
    },
    deleteFollowed() {
      this.user = {
        ...this.user,
        isFollowed: false,
      };
    },
  },
  created() {},
};
</script>